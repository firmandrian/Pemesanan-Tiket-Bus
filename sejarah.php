<center><h2><b>SEJARAH</b></h2></center>
<br><br>
<p>SEJARAH
Berawal dari pasangan suami istri yang sederhana, Bapak Yustinus Soeroso dan Ibu Yustina Rahyuni Soeroso mengawali perjuangannya mendirikan usaha transportasi tahun 1983 yang waktu itu hanya 1 armada jenis Colt Diesel AD 9866 A, yang sekarang disebut dengan “Bibit Kawit” dengan membawa penumpang Solo – Blitar. Setelah itu berlanjut usaha travel dengan trayek/rute Jogja – Surabaya, Jogja – Blitar/Malang dan terus berkembang sampai tahun 1991.Pada tahun tersebut (1991) barulah mempunyai 5 armada “Bumel Non AC” dengan merk HINO type AK, dan sejak saat itu secara resmi Perusahaan Otobis (PO) Rosalia Indah resmi menjadi perusahaan perseorangan dengan ijin usaha Biro Perjalanan Umum disingkat BPU. Rosalia Indah dengan No. 05/D.2/BPU/III/1991 dari Deparpostel Dirjend Pariwisata.</p>
<br><br>
<p>Seiring perkembangan dunia usaha transportasi, maka perusahaan perseorangan (PO) dituntut harus profesional dalam pengelolaan manajemen dan pelayanan, sehingga tepatnya tanggal 15 April 2015, PO. Rosalia Indah berubah menjadi perusahaan berbadan hukum dengan nama PT. Rosalia Indah Transport berdasarkan SK. Menkumham RI No. AHU-2392920.AH.01.01. yang beralamat di Jl. Raya Solo - Sragen KM. 7,5 Jaten, Karanganyar, Jawa Tengah. PT. Rosalia Indah Transport adalah sebuah perusahaan yang sedang berkembang pesat dan merupakan perusahaan swasta yang diperhitungkan dalam percaturan bisnis jasa angkutan darat di Indonesia. Ketatnya kondisi persaingan bisnis transportasi darat sejak era 1990-an sampai saat ini bukan menjadi kendala bagi PT. Rosalia Indah Transport, bahkan pada masa itu dijadikan titik tolak dari yang semula berorientasi pada pelayanan transportasi AKDP (Antar Kota Dalam Provinsi) menjadi AKAP (Antar Kota Antar Provinsi) yang lebih memiliki daya jangkau luas, lebih handal dan lebih mantap hingga saat ini, dengan sumber daya manusia lebih dari 1.000 personil dan lebih dari 140 kantor perwakilan dan agen Rosalia Indah tersebar di Jawa - Sumatera. Bahkan PT. Rosalia Indah Transport juga siap melayani pariwisata dan carter bis pariwisata dengan armada pariwisata.</p>
